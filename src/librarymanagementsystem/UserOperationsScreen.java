      public void userShow(){
        model.setRowCount(0);
        ArrayList<Users> users = new ArrayList<Users>();
        users = islemler.getUsers();
        if (users != null) {
            for(Users user :users ){
                Object[] add = {user.getId(), user.getName(), user.getSurname(), user.getUsername(), user.getEmail(), user.getdate_of_membership(), user.getPhone(), user.getAddress()};
                  model.addRow(add); 
            }
        }
    }
        public void dynamicSearch(String search){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        jTable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(search));
        
    }
    
