    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            jLabel8.setText("");
           String name = this.name.getText();
           String surname = this.surname.getText();
           String username = this.username.getText();
           String email = this.email.getText();
           String date_of_membership = this.date_of_membership.getText();
           String phone = this.phone.getText();
           String address = this.address.getText();
           int selected_row = jTable1.getSelectedRow();
           
           if (selected_row == -1) {
               if (model.getRowCount() == 0) {
                     jLabel8.setText("Table is empty");
               }else{
                   jLabel8.setText("Please select user");
               }
        }else{
               int id = (int) model.getValueAt(selected_row, 0);
               islemler.updateUser(id, name, surname, username, email, date_of_membership, phone, address);
               userShow(); 
               jLabel8.setText("User updated");
           }
    }//GEN-LAST:event_jButton2ActionPerformed

